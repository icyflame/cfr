The objectives of our research are (1) to develop intelligent sensor-based robotic system 

for material handling and assembly applications and (2) to develop sensor integrated 

multi-finger robot grippers for applications in manufacturing. Industrial robots will be 

equipped with tactile and non-tactile sensors and AI-based collision detection algorithms 

will be developed for obstacle avoidance. Vision sensing will be developed to position 

the robot manipulator and gripper coupled with tactile sensing based on force/torque 

sensors to guide the robot during assembly. Further, multi-finger robot grippers will be 

developed with various types of tactile and non-tactile sensors to augment their sensory 

capabilities and provide guidance in grasping and manipulation.
