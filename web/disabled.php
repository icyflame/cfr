Rehabilitation robotics plays an important role in taking care of older people to regain 

their physical activity. Orthotic devices are used as artificial instruments to support the 

skeletal system to modify proper functional characteristics of neuromuscular activity. 

The different applications of orthotic devices are: controlling, guiding or immobilizing an 

extremity, joint or body segment for walking, stair-case ascending-descending etc. The 

patients for whom, recovery is possible, these devices may be used by physiotherapist to 

restore the limb’s original movement by proper training with the device. Though a 

number of orthotic devices have been developed, there are some issues which are 

required to be incorporated in those devices. The issues are as follows: Most of the 

devices that were developed were relatively heavy and involved high cost, which are 

difficult to use and expensive for common people. Design of intelligent controller to 

minimize the interaction constraint between the user and the device must be carried out. 

Implementation of adaptive control system for enhancing the patient’s adaptability to 

those devices is also required. Earlier devices were tested in treadmill walking instead of 

real walking. Optimal design and development of orthotic device for knee and hip joints 

to assist disabled person in normal walking will be carried out based on human walking 

dynamics. Design and implementation of an appropriate adaptive controller to 

incorporate intelligence and flexibility in the device will also be carried out. 

Experimental validation of the orthotic devices during a normal walking cycle will be 

achieved.
